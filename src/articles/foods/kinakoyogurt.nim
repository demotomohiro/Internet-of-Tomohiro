## きな粉のヨーグルトを作るメリット

### 安い

業務用スーパーで1kgのきな粉が約300円で売られています。
きな粉のヨーグルトはきな粉1kgに水を約2kg加えて3kg作ることができます。
なので1kgあたり約100円で作ることができます。
牛乳や豆乳が1ℓあたり100円で売られることはあまりないと思うので牛乳や豆乳からヨーグルトを作るときより安くなります。
(しかしスーパーによってはそれほど安いきな粉が売られてなかったりするようですが)

また安くたんぱく質を得ることができます。
きな粉の袋の栄養成分表示によると100gあたり36.7gのたんぱく質が含まれています。
よって100gあたりのたんぱく質の値段は300円 / (36.7g * 10) * 100 = 81.7円/100gとなります。
鶏卵100gあたりのたんぱく質は12.2g、Mサイズの卵は約60gなので10個入りが120円だとすると
100gあたりのたんぱく質の値段は120円 / (12.2g / 100g * 60g * 10) * 100 = 164円/100gとなります。
鶏むね肉皮付きの100gあたりのたんぱく質は21.3gなので2kgが900円で売っていたとすると
100gあたりのたんぱく質の値段は900円 / (21.3g / 100g * 2000g) * 100 = 211円/100gとなります。

### 水分の割合を調整できる

きな粉は乾燥した状態で売られているのでヨーグルトにするには水分を加える必要があります。
加える水の量を減らすだけで濃厚で固めにできます。

### 栄養

栄養士ではないのできな粉のヨーグルトに含まれる栄養成分や体にどのような影響があるのかは正直わかりません。
きな粉は大豆を炒めて皮をむき粉にしたものなので、皮を除いた大豆そのままと言えます。
`豆乳 <https://ja.wikipedia.org/wiki/%E8%B1%86%E4%B9%B3>`_ は、大豆を水に浸してすりつぶし、水を加えて煮つめた汁を漉したものです。煮詰めた汁を濾して残ったのがおからです。
ですのできな粉のヨーグルトの栄養成分は豆乳ヨーグルトとおからを合わせたものとほぼ同じではないかと思います。

### その他

- 材料のきな粉は常温で保存可能。未開封であれば10ヶ月ぐらいもつようです

## きな粉のヨーグルトを作る方法

### 材料

- きな粉
- 水
  - きな粉の質量の2倍
- 乳酸菌を含んだ食品(スーパーで売っている大豆ヨーグルトなど)
  - 自分はmarusanの豆乳グルトを使っています
  - 作ったヨーグルトを繰り返し種に使うと雑菌が増えることがあるらしいので避けたほうがいいでしょう。 

### 必要な物

- きな粉のヨーグルトを入れる清潔な袋または容器
  - 発酵途中で気体が発生して膨張する場合があるので大き目のものを用意したほうがいいです。

### 作り方

きな粉と水を混ぜた後は牛乳や豆乳からヨーグルトを作るときとほぼ同じです。

1. 容器にきな粉と水と乳酸菌を含んだ食品を入れて全体が均一になるまで混ぜます。
   豆乳ヨーグルトはきな粉1kgあたりスプーン大さじ5杯ぐらい入れます。
   一度に水を全部いれるより半分つづ入れたほうが混ぜやすいです。

   .. image:: https://gist.githubusercontent.com/demotomohiro/f3ae06fe68f32fa9626f429b6e3f33ec/raw/74dcb3267bee0a4e6a1e42a62c43f9fc84da2b53/inbag.jpg

1. 発酵させます。
   約40度に温めて乳酸菌を増やします。
   45度ぐらいに温めたお湯に入れるかヨーグルトメーカなどを使って保温します。
   数時間温めて乳酸菌が増えれば室温ぐらいでも発酵するようです。
   このときに気体が発生して容器が膨らむことがあります。
   marusanの豆乳グルトを使うと発生することはあまりありませんが、それでも発生するときがあります。
   容器が破裂しないように中の空気を抜いたりヨーグルト内の気体を混ぜて取り除きましょう。

1. 発酵したら冷蔵庫に入れましょう。

完成したら砂糖、はちみつ、果物などを入れて食べましょう。

## 水以外の飲み物からきな粉のヨーグルトを作れるか

上で紹介したきな粉のヨーグルトはきな粉に水を混ぜてから発酵させますが、水の代わりに別の飲み物を混ぜるとどうなるのでしょうか。
以下の飲み物を使ってきな粉のヨーグルトを作ってみました。

- 牛乳

  一般的なヨーグルトは牛乳を発酵させて作ります。
  きな粉と牛乳を混ぜて発酵することができれば濃厚な牛乳ときな粉のヨーグルトができるかもしれません。

