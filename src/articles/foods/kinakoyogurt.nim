## きな粉のヨーグルトを作る方法を見つけたきっかけ

業務用スーパーできな粉1kgが約300円と安く売られていたのでこれを使って何かおいしいものを作れないかと考えていました。
最初はきな粉から納豆を作ろうと納豆ときな粉と水を混ぜて瓶に入れて蓋をして発酵させたのですが
納豆にならずヨーグルトのような味のものができました。
後で調べたところ、納豆菌は空気がないと繁殖できない好気性で乳酸菌は増殖に酸素を必要としない嫌気性らしいです。
きな粉と水を混ぜて加熱した後に納豆を加えて空気に触れるようにしたところ、表面が白っぽくなり納豆のようなものができました。
しかし市販の納豆よりだいぶ臭くなったのでこれ以上作るのをやめました。
豆乳からヨーグルトが作れるなら同じ大豆から作られたきな粉でもヨーグルトが作れるはずだと思い納豆の代わりにヨーグルトを作ることにしました。

